public class Lingkaran {
    
    double hitungLuas(int jari){
        return(Math.PI * jari * jari);
    }
    
    double hitungLuas(double jari){
        return(Math.PI * jari * jari);
    }
    
    double hitungKeliling(int jari){
        return(Math.PI * (jari + jari));
    }
    
    double hitungKeliling(double jari){
        return(Math.PI * (jari + jari));
    }
    
}
